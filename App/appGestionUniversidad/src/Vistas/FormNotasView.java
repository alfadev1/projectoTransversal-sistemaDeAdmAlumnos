    public void borrarFilas() {
        int f = modelo.getRowCount() - 1;
        for (; f >= 0; f--) {
            modelo.removeRow(f);
        }
    }

    private void llenarCombo() {
        List<Alumno> lista = ad.listarAlumnos();
        jCBAlum.removeAllItems();
        for (Alumno alumno : lista) {
            jCBAlum.addItem(alumno);
        }

    }
    
